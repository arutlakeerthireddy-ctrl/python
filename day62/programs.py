#Write a program to find the minimum value in a list using min().
li=[4,5,8,0,1,3]
def minimum_value():
    return min(li)
print(minimum_value())

#Use abs() to convert negative numbers to positive.
def convert(a):
    return abs(a)
print(convert(-2)) #2

#Write a program using round() to round decimal values.
f=10.6
def decimal_values():
    return round(f)
print(decimal_values())

#Use sorted() on a list and a string.
