#keyword arguments
#arguments that pass to a function using parameter names (dont have order)
#examples
def greet(name,age):
    print(name,age)
greet(name="keerthi",age=21) #keerthi 21

def rectangle(len,bre):
    print(len*bre)
rectangle(len=5,bre=6) #30
rectangle(bre=6,len=5) #30

def fun(a,b):
    print(a+b)
x=5
y=4
fun(b=x,a=y) #9  order doesnt matter





