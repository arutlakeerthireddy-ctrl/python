#builtin functions in dictionary
#len()
d={"name":"keerthi","branch":"csm","course":"Btech"}
print(len(d))#3

#type()
print(type(d))#<class 'dict'>

#sorted()
print(sorted(d))#['branch', 'course', 'name']

#max()
print(max(d))#name

#min()
print(min(d))#branch

#sum()
d={1:2,5:6,3:9}
print(sum(d))#9